function ReadOnlyUserDetails(props: UserDetailsProps) {

  const getIconForKey = (key: string) => {
    switch (key) {
      case 'company':
        return <CompanyIcon />;
      case 'country':
        return <CountryIcon />;
      case 'created-date':
        return <CreatedDateIcon />;
      case 'phone-number':
        return <PhoneNumberIcon />;
      default:
        return null;
    }
  };

  const getFormattedDate = (): string => {
    const formattedDate = new Intl.DateTimeFormat("en-GB", {
      year: "numeric",
      month: "short",
      day: "2-digit",
      hour: "2-digit",
      minute: "2-digit",
      timeZoneName: "short"
    }).format(new Date(props.data["created-date"] as Date));

    return formattedDate;
  };

  const getProfilePicture = (): string => {
    return props.data["profile-picture"] !== null && props.data["profile-picture"] !== '' ? props.data["profile-picture"] : defaultProfilePicture;
  };

  return (
    <>
      <div className="user-profile-header">
        <div className="header-action-button right edit" onClick={props.actions.edit}>
          <EditIcon />
        </div>
      </div>
      <div className="user-info">
        <div className="profile-image" style={{ backgroundImage: `url(${getProfilePicture()})` }}></div>
        <h2 className="user-fullname">{`${props.data["first-name"] === '' ? 'Placeholder' : props.data["first-name"]} ${props.data["last-name"] === '' ? 'Name' : props.data["last-name"]}`}</h2>
        <span className="user-email">{`${props.data["email"] === '' ? 'placeholdername@example.com' : props.data["email"]}`}</span>
      </div>
      <div className="user-details">
        {Object.entries(props.data).filter(([key]) => key !== 'first-name' && key !== 'last-name' && key !== 'email' && key !== 'profile-picture').map(([key, value]) => (
          <div key={key} className={`field ${key}`}>
            <div className="field-label">
              {getIconForKey(key)}
              <span id={key}>{key.replace('-', ' ')}</span>
            </div>
          
            <span id="value">{key === 'created-date' ? getFormattedDate() : value === '' || value === null ? 'Not set' : value }</span>
          </div>
        ))}
      </div>
      <div className="user-actions">
        <button id="deactivate" onClick={props.actions.deactivateUser}>Deactivate</button>
        <button id="logout" onClick={props.actions.logout}>Log out</button>
      </div>
    </>
  );
};

export default ReadOnlyUserDetails;